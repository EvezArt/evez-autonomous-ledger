#!/usr/bin/env python3
"""
EVEZ Code Generator — Builder Module

Creates and commits files to repos via GitHub Contents API.
No cloning needed — everything goes through the REST API.
"""

import os
import json
import time
import base64
import datetime
import sys

try:
    import requests
except ImportError:
    print("ERROR: 'requests' not available.")
    sys.exit(0)

GITHUB_TOKEN = os.environ.get("GITHUB_TOKEN", "")
ORG = os.environ.get("REPO_OWNER", "EvezArt")
API = "https://api.github.com"
HEADERS = {
    "Authorization": f"Bearer {GITHUB_TOKEN}",
    "Accept": "application/vnd.github+json",
    "X-GitHub-Api-Version": "2022-11-28",
}
API_DELAY = 0.5


def utcnow():
    return datetime.datetime.utcnow().strftime("%Y-%m-%dT%H:%M:%SZ")


def _api(method, url, **kwargs):
    time.sleep(API_DELAY)
    try:
        resp = getattr(requests, method)(url, headers=HEADERS, timeout=30, **kwargs)
        return resp
    except Exception as exc:
        print(f"  ⚠ API {method.upper()} {url}: {exc}")
        return None


def commit_file(repo, path, content, message, branch=None):
    """Create or update a file in a repo via GitHub Contents API."""
    url = f"{API}/repos/{ORG}/{repo}/contents/{path}"
    params = {}
    if branch:
        params["ref"] = branch

    # Check existing (need sha for updates)
    existing = _api("get", url, params=params)
    sha = None
    if existing and existing.status_code == 200:
        sha = existing.json().get("sha")

    payload = {
        "message": message,
        "content": base64.b64encode(content.encode()).decode(),
    }
    if sha:
        payload["sha"] = sha
    if branch:
        payload["branch"] = branch

    resp = _api("put", url, json=payload)
    if resp and resp.status_code in (200, 201):
        action = "updated" if sha else "created"
        print(f"    ✅ {action} {repo}/{path}")
        return True
    else:
        status = resp.status_code if resp else "no response"
        print(f"    ❌ Failed to commit {repo}/{path} (status={status})")
        return False


