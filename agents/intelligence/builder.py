def build_missing_files(repos_missing, repo_metadata=None):
    """Given analysis of missing files, generate and commit them.

    Args:
        repos_missing: list of {"repo": str, "missing": [str]}
        repo_metadata: optional dict of {repo_name: {"description": str, "language": str}}
    """
    if repo_metadata is None:
        repo_metadata = {}

    results = []
    for entry in repos_missing:
        repo = entry["repo"]
        meta = repo_metadata.get(repo, {})
        language = meta.get("language")
        description = meta.get("description", "")

        for missing_file in entry["missing"]:
            content = None
            path = None
            message = None

            if missing_file == "README.md":
                content = generate_readme(repo, description)
                path = "README.md"
                message = "docs: add README.md (auto-generated)"

            elif missing_file == ".gitignore":
                content = generate_gitignore(language)
                path = ".gitignore"
                message = "chore: add .gitignore (auto-generated)"

            elif missing_file == "CI workflows":
                content = generate_ci_workflow(repo, language)
                path = ".github/workflows/ci.yml"
                message = "ci: add basic CI workflow (auto-generated)"

            elif missing_file == "CONTRIBUTING.md":
                content = generate_contributing()
                path = "CONTRIBUTING.md"
                message = "docs: add CONTRIBUTING.md (auto-generated)"

            elif missing_file == "CODE_OF_CONDUCT.md":
                content = generate_code_of_conduct()
                path = "CODE_OF_CONDUCT.md"
                message = "docs: add CODE_OF_CONDUCT.md (auto-generated)"

            if content and path:
                ok = commit_file(repo, path, content, message)
                results.append({
                    "repo": repo,
                    "file": path,
                    "success": ok,
                    "timestamp": utcnow(),
                })

    return results


